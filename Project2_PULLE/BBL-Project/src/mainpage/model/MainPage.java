package mainpage.model;

import java.util.Date;

public class MainPage {
	private int mainpage_ID;
	private String user_ID;
	private String mainpage_Title;
	private String mainpage_Content;
	private Date mainpage_Date;
	private String mainpage_Photo;
	private String mainpage_Loc;
	
	public int getMainpage_ID() {
		return mainpage_ID;
	}
	public void setMainpage_ID(int mainpage_ID) {
		this.mainpage_ID = mainpage_ID;
	}
	public String getUser_ID() {
		return user_ID;
	}
	public void setUser_ID(String user_ID) {
		this.user_ID = user_ID;
	}
	public String getMainpage_Title() {
		return mainpage_Title;
	}
	public void setMainpage_Title(String mainpage_Title) {
		this.mainpage_Title = mainpage_Title;
	}
	public String getMainpage_Content() {
		return mainpage_Content;
	}
	public void setMainpage_Content(String mainpage_Content) {
		this.mainpage_Content = mainpage_Content;
	}
	public Date getMainpage_Date() {
		return mainpage_Date;
	}
	public void setMainpage_Date(Date mainpage_Date) {
		this.mainpage_Date = mainpage_Date;
	}
	public String getMainpage_Photo() {
		return mainpage_Photo;
	}
	public void setMainpage_Photo(String mainpage_Photo) {
		this.mainpage_Photo = mainpage_Photo;
	}
	public String getMainpage_Loc() {
		return mainpage_Loc;
	}
	public void setMainpage_Loc(String mainpage_Loc) {
		this.mainpage_Loc = mainpage_Loc;
	}
	
	
}
